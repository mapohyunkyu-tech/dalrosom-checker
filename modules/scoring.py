def estimate_price(score):
    if score >= 94:
        return "5만 원 이상급 가능"
    if score >= 92:
        return "4~5만 원급 테스트/포폴 원고"
    if score >= 89:
        return "4만 원급 가능"
    if score >= 86:
        return "3만 원급 가능"
    if score >= 80:
        return "1.5~2만 원급 저단가 납품 가능"
    return "수정 필요"


def final_judgement(score):
    if score >= 92:
        return "제출 가능 수준입니다. 작은 표현만 다듬으면 됩니다.", "success"
